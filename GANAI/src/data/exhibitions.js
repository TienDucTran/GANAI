export const getExhibitionById = (id) => {
    return exhibitionsData[id] || null;
};

export const getAllExhibitions = () => {
    return Object.values(exhibitionsData);
};

export const getExhibitionsByCategory = (category) => {
    if (category === 'all') {
        return getAllExhibitions();
    }
    return getAllExhibitions().filter(
        (exhibition) => exhibition.category === category,
    );
};

export const getExhibitionsByStatus = (status) => {
    return getAllExhibitions().filter(
        (exhibition) => exhibition.status === status,
    );
};

export const getCategories = () => {
    const categories = new Set(
        getAllExhibitions().map((exhibition) => exhibition.category),
    );
    return ['all', ...Array.from(categories)];
};
